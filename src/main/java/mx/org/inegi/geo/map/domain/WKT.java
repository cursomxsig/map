package mx.org.inegi.geo.map.domain;

/**
 * @author femat
 *
 */
public class WKT {

	private int id;

	private String wkt;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the wkt
	 */
	public String getWkt() {
		return wkt;
	}

	/**
	 * @param wkt
	 *            the wkt to set
	 */
	public void setWkt(String wkt) {
		this.wkt = wkt;
	}

}
